function apple() {
    var table = document.querySelector('#table');
    var q = parseInt(document.querySelector('#received').value);
    document.querySelector('#received1').innerHTML = q;
    var h = parseInt(document.querySelector('#onHand').value);
    document.querySelector('#onHand1').innerHTML = h; 
    var total1 = (q + h);
    document.querySelector('#total1').innerHTML = total1;
}
